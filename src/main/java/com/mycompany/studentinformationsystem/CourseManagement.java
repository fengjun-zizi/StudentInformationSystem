    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         String courseId = jTextFieldCourseID.getText();
    if (courseId.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please select a course to delete!");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this course?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/student_db", "root", "");
            String sql = "DELETE FROM courses WHERE course_id = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, courseId);

            int rows = pstmt.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Course Deleted Successfully!");
                loadCourseData();  // üîÑ Âà∑Êñ∞Êï∞ÊçÆ
                clearFields();     // üîÑ Ê∏ÖÁ©∫ËæìÂÖ•Ê°Ü
            } else {
                JOptionPane.showMessageDialog(this, "Failed to Delete Course");
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

