    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
String studentId = jTextFieldStudentID.getText();
    String courseId = jTextFieldCourseID.getText();
    String grade = jTextFieldGrade.getText();

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/student_db", "root", "");
        String sql = "INSERT INTO grades (student_id, course_id, grade) VALUES (?, ?, ?)";
        PreparedStatement pstmt = conn.prepareStatement(sql);
        pstmt.setString(1, studentId);
        pstmt.setString(2, courseId);
        pstmt.setString(3, grade);

        int rows = pstmt.executeUpdate();
        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Grade Added Successfully!");
            loadGradeData();
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to Add Grade");
        }
        conn.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

