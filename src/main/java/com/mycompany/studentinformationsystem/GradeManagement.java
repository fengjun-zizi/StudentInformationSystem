    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
String courseId = jTextFieldCourseID.getText();
    String studentId = jTextFieldStudentID.getText();
    
    // Âà§Êñ≠ÊòØÂê¶‰∏∫Á©∫
    if (courseId.isEmpty() || studentId.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter both Course ID and Student ID to delete a grade!");
        return;
    }

    // Á°ÆËÆ§ÊòØÂê¶Âà†Èô§
    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this grade?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            // Âª∫Á´ãÊï∞ÊçÆÂ∫ìËøûÊé•
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/student_db", "root", "");

            // SQL Âà†Èô§ËØ≠Âè•
            String sql = "DELETE FROM grades WHERE course_id = ? AND student_id = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, courseId);
            pstmt.setString(2, studentId);

            // ÊâßË°åÂà†Èô§
            int rows = pstmt.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Grade Deleted Successfully!");
                loadGradeData();  // üîÑ Âà∑Êñ∞Êï∞ÊçÆ
                clearFields();    // üîÑ Ê∏ÖÁ©∫ËæìÂÖ•Ê°Ü
            } else {
                JOptionPane.showMessageDialog(this, "Failed to Delete Grade. Record not found.");
            }
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

